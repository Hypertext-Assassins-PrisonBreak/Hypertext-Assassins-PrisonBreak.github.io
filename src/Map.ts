  private canvas: HTMLCanvasElement;

  /**
   * Construc a new instance of this class
   *
   * @param canvas the canvas to render on
   */
  public constructor(canvas: HTMLElement) {
    this.canvas = <HTMLCanvasElement>canvas;
    // Resize the canvas to full window size
    this.canvas.width = window.innerWidth - 1;
    this.canvas.height = window.innerHeight - 4;
    this.render();
  }

  /**
   * Draws the game to the canvas
   */
  public render(): void {
    // Get the canvas rendering context
    const ctx = this.canvas.getContext('2d');
    // Clear the entire canvas
    ctx.clearRect(0, 0, this.canvas.width, this.canvas.height);

    ctx.font = 'bold 10pt sans-serif';

    const sec = Math.floor(Date.now() / 1000);

    if (sec !== this.currentSecond) {
      this.currentSecond = sec;
      this.framesLastSecond = this.frameCount;
      this.frameCount = 1;
    } else { this.frameCount += 1; }

    console.log(this.frameCount);

    for (let y = 0; y < Map.mapH; y++) {
      for (let x = 0; x < Map.mapW; x++) {
        switch (this.gameMap[((y * Map.mapW) + x)]) {
          case 1:
            ctx.fillStyle = '#999999';
            break;
          default:
            ctx.fillStyle = '#eeeeee';
        }
        ctx.fillRect(x * Map.tileW, y * Map.tileH, Map.tileW, Map.tileH);
      }
    }
    ctx.fillStyle = '#ff0000';
    ctx.fillText(`FPS: ${this.framesLastSecond}`, 10, 20);
  }
